    private void kendaraanTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kendaraanTableMouseClicked
        try {
            if(evt.getClickCount() == 2){
                int row = kendaraanTable.getSelectedRow();
                String platPatokan = kendaraanTable.getValueAt(row, 1).toString();
                
                DetailData detail = new DetailData();
                detail.setPlat(platPatokan);
                detail.setVisible(true);
                detail.setList(this.listKend);
                detail.setField();
                Kendaraan currKend = listKend.linearSearch(platPatokan);;
                if(currKend.getStatus().equals("keluar")){
                    detail.getCheckoutButton().setVisible(false);
                }
                
                dispose();
            }
        } catch (Throwable t){
            JOptionPane.showMessageDialog(null,"Record Kosong");
        }

    }//GEN-LAST:event_kendaraanTableMouseClicked

