    private void rowToForm() {
        int kolom = 1;
        int row = kendaraanTable.getSelectedRow();
        String platPatokan = kendaraanTable.getValueAt(row, 1).toString();
        Kendaraan rowKend = this.listKend.linearSearch(platPatokan);
        
        if (rowKend.getJenis().equals("motor")){
            motorRadioButton.setSelected(true);
            mobilRadioButton.setSelected(false);
            trukRadioButton.setSelected(false);
        } else if (rowKend.getJenis().equals("mobil")){
            motorRadioButton.setSelected(false);
            mobilRadioButton.setSelected(true);
            trukRadioButton.setSelected(false);
        } else if (rowKend.getJenis().equals("truk")){
            motorRadioButton.setSelected(false);
            mobilRadioButton.setSelected(false);
            trukRadioButton.setSelected(true);
        }
        platField.setText(platPatokan);
        brandField.setText(rowKend.getNamaBrand());
        bpkbField.setText(rowKend.getBpkb());
        stnkField.setText(rowKend.getStnk());
        namaField.setText(rowKend.getPersons().getNama());
        simField.setText(rowKend.getPersons().getSim());        
        ktpField.setText(rowKend.getPersons().getKtp());   
        if (rowKend.getPersons().getJk().equals("L")){
            lRadioButton.setSelected(true);
            pRadioButton.setSelected(false);
        } else if (rowKend.getPersons().getJk().equals("P")){
            lRadioButton.setSelected(false);
            pRadioButton.setSelected(true);
        }   
        umurField.setText(String.valueOf(rowKend.getPersons().getUmur()));   
        
        setEnabledKomponen();
        ubahButton.setEnabled(false);
        hapusButton.setEnabled(false);
        makeButton.setEnabled(false);
        simpanButton.setEnabled(true); 
        batalButton.setEnabled(true);
    }
    
