    private void setTable(){
        int count = 1, total = 0, row = 0;
        
        Kendaraan bantu = listKend.getKendaraan();
        while (bantu != null) {
            int kolom = 0;
            Waktu durasiPark = bantu.getKeluar().hitungDurasiParkir(bantu.getDatang()); // Passing object
            String status = bantu.getStatus();

            kendaraanTable.setValueAt(count++, row, kolom++);
            kendaraanTable.setValueAt(bantu.getPlatKend(), row, kolom++);
            kendaraanTable.setValueAt(bantu.getJenis(), row, kolom++);
            kendaraanTable.setValueAt(bantu.getDatang().konversiToMilitary(), row, kolom++);
            if(status.equalsIgnoreCase("keluar")){
                kendaraanTable.setValueAt(bantu.getKeluar().konversiToMilitary(), row, kolom++);
                kendaraanTable.setValueAt(bantu.getKeluar().jamParkir(durasiPark), row, kolom++);
                kendaraanTable.setValueAt("Rp. " + bantu.biayaTotal(), row, kolom++);
                total += bantu.biayaTotal();
            } else if(status.equalsIgnoreCase("parkir")){
                kendaraanTable.setValueAt(" ", row, kolom++);
                kendaraanTable.setValueAt(" ", row, kolom++);
                kendaraanTable.setValueAt(" ", row, kolom++);
            }
            kendaraanTable.setValueAt(status, row++, kolom);
            bantu = bantu.next;
        } 
        this.row = row;
        this.totalBiaya = total;
        totalBiayaLabel.setText(String.valueOf(this.totalBiaya));
    }
    
