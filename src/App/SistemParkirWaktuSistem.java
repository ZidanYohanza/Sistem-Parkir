    private void simpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanButtonActionPerformed
        int barisSimpan, row, kolom = 0;
        
        ubahButton.setEnabled(true);
        hapusButton.setEnabled(true);
        makeButton.setEnabled(true);
        simpanButton.setEnabled(false);
        batalButton.setEnabled(false);
        
        if(inputIsEmpty()){
            JOptionPane.showMessageDialog(null,"Masih ada nilai yang kosong, silakan dilengkapi");
            motorRadioButton.requestFocus();
        }
        else {
            String jk = "";
            boolean gagal = false;
            
            if(lRadioButton.isSelected()){
                jk = "L";
            } else if (pRadioButton.isSelected()){
                jk = "P";
            }
            
            do{
                try{
                    String plat = platField.getText();
                    Kendaraan bantu =  listKend.linearSearch(plat);
                    bantu.setPlatKend(platField.getText());
                    bantu.setNamaBrand(brandField.getText());
                    bantu.setBpkb(bpkbField.getText());
                    bantu.setStnk(stnkField.getText());

                    // Proses input person
                    bantu.getPersons().setNama(namaField.getText());
                    bantu.getPersons().setSim(simField.getText());
                    bantu.getPersons().setKtp(ktpField.getText());
                    bantu.getPersons().setJk(jk);
                    int umur = Integer.parseInt(umurField.getText());
                    bantu.getPersons().setUmur(umur);
                    
                } catch (Throwable e) {
                    JOptionPane.showMessageDialog(null, "Nilai non-numeric, silakan input nilai numeric!");
                    gagal = true;
                    return;
                }
            } while (gagal);
            
            setTable();   
            clearKendaraan();
            clearPerson();
            setDisabledKomponen();
        }
    }//GEN-LAST:event_simpanButtonActionPerformed

