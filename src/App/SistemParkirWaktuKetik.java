    private void kendaraanTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kendaraanTableMouseClicked
        try {
            if(evt.getClickCount() == 2){
                rowToForm();
                setEnabledKomponen();
                motorRadioButton.setEnabled(false); 
                mobilRadioButton.setEnabled(false); 
                trukRadioButton.setEnabled(false); 
                platField.requestFocus();
                kendaraanTable.setEnabled(false);

                ubahButton.setEnabled(false);
                hapusButton.setEnabled(false);
                makeButton.setEnabled(false);
                simpanButton.setEnabled(true); 
                batalButton.setEnabled(true);
            }
        } catch (Throwable t){
            JOptionPane.showMessageDialog(null,"Record Kosong");
        }

    }//GEN-LAST:event_kendaraanTableMouseClicked

