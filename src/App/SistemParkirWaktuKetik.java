    private void setTable(){
        int count = 1, total = 0, row = 0;
        
        Kendaraan bantu = listKend.getKendaraan();
        if(bantu !=  null){
            do {
                int kolom = 0;
                Waktu durasiPark = bantu.getKeluar().hitungDurasiParkir(bantu.getDatang()); // Passing object
                
                kendaraanTable.setValueAt(count++, row, kolom++);
                kendaraanTable.setValueAt(bantu.getJenis(), row, kolom++);
                kendaraanTable.setValueAt(bantu.getPlatKend(), row, kolom++);
                kendaraanTable.setValueAt(bantu.getNamaBrand(), row, kolom++);
                kendaraanTable.setValueAt(bantu.getBpkb(), row, kolom++);
                kendaraanTable.setValueAt(bantu.getStnk(), row, kolom++);
                kendaraanTable.setValueAt(bantu.getDatang().konversiToMilitary(), row, kolom++);
                kendaraanTable.setValueAt(bantu.getKeluar().konversiToMilitary(), row, kolom++);
                kendaraanTable.setValueAt(durasiPark.konversiToMilitary(), row, kolom++);
                kendaraanTable.setValueAt(bantu.getKeluar().jamParkir(durasiPark), row, kolom++);
                kendaraanTable.setValueAt(bantu.biayaTotal(), row, kolom++);
                
                kendaraanTable.setValueAt(bantu.getPersons().getNama(), row, kolom++);
                kendaraanTable.setValueAt(bantu.getPersons().getSim(), row, kolom++);
                kendaraanTable.setValueAt(bantu.getPersons().getKtp(), row, kolom++);
                kendaraanTable.setValueAt(bantu.getPersons().getJk(), row, kolom++);
                kendaraanTable.setValueAt(bantu.getPersons().getUmur(), row++, kolom++);
                
                total += bantu.biayaTotal();
                bantu = bantu.next;
            } while (bantu != null);
        }
        this.row = row;
        this.totalBiaya = total;
        totalBiayaLabel.setText(String.valueOf(this.totalBiaya));
    }
    
