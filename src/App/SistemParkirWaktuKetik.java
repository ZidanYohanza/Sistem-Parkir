    private void rowToForm() {
        int kolom = 1;
        int row = kendaraanTable.getSelectedRow();
        
        if (kendaraanTable.getValueAt(row, kolom).toString().equals("motor")){
            motorRadioButton.setSelected(true);
            mobilRadioButton.setSelected(false);
            trukRadioButton.setSelected(false);
        } else if (kendaraanTable.getValueAt(row, kolom).toString().equals("mobil")){
            motorRadioButton.setSelected(false);
            mobilRadioButton.setSelected(true);
            trukRadioButton.setSelected(false);
        } else if (kendaraanTable.getValueAt(row, kolom).toString().equals("truk")){
            motorRadioButton.setSelected(false);
            mobilRadioButton.setSelected(false);
            trukRadioButton.setSelected(true);
        }
        kolom++;
        platField.setText(kendaraanTable.getValueAt(row, kolom++).toString());
        brandField.setText(kendaraanTable.getValueAt(row, kolom++).toString());
        bpkbField.setText(kendaraanTable.getValueAt(row, kolom++).toString());
        stnkField.setText(kendaraanTable.getValueAt(row, kolom++).toString());
        jamMasukField.setText(kendaraanTable.getValueAt(row, kolom).toString().substring(0, 2));
        menitMasukField.setText(kendaraanTable.getValueAt(row, kolom).toString().substring(3, 5));
        detikMasukField.setText(kendaraanTable.getValueAt(row, kolom++).toString().substring(6, 8));
        jamKeluarField.setText(kendaraanTable.getValueAt(row, kolom).toString().substring(0, 2));
        menitKeluarField.setText(kendaraanTable.getValueAt(row, kolom).toString().substring(3, 5));
        detikKeluarField.setText(kendaraanTable.getValueAt(row, kolom++).toString().substring(6, 8));
        kolom += 3;
        namaField.setText(kendaraanTable.getValueAt(row, kolom++).toString());
        simField.setText(kendaraanTable.getValueAt(row, kolom++).toString());        
        ktpField.setText(kendaraanTable.getValueAt(row, kolom++).toString());   
        if (kendaraanTable.getValueAt(row, kolom).toString().equals("L")){
            lRadioButton.setSelected(true);
            pRadioButton.setSelected(false);
        } else if (kendaraanTable.getValueAt(row, kolom).toString().equals("P")){
            lRadioButton.setSelected(false);
            pRadioButton.setSelected(true);
        }   
        kolom++;
        umurField.setText(kendaraanTable.getValueAt(row, kolom++).toString());   
        
        setEnabledKomponen();
        ubahButton.setEnabled(false);
        hapusButton.setEnabled(false);
        makeButton.setEnabled(false);
        simpanButton.setEnabled(true); 
        batalButton.setEnabled(true);
    }
    
